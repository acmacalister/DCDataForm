#import "DCDateItem.h"

@implementation DCDateItem

////////////////////////////////////////////////////////////////////////////////////////////////////
- (instancetype)initWithDate:(NSDate *)date
{
    if(self = [super init])
    {
        self.date = date;
    }
    
    return self;
}
////////////////////////////////////////////////////////////////////////////////////////////////////
+(instancetype)itemWithDate:(NSDate *)date
{
    return [[DCDateItem alloc] initWithDate:date];
}
////////////////////////////////////////////////////////////////////////////////////////////////////

@end
